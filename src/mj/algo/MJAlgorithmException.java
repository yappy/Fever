package mj.algo;

/**
 * @author yappy
 */
public class MJAlgorithmException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public MJAlgorithmException() {
	}

	public MJAlgorithmException(String message) {
		super(message);
	}

	public MJAlgorithmException(Throwable cause) {
		super(cause);
	}

	public MJAlgorithmException(String message, Throwable cause) {
		super(message, cause);
	}

}
